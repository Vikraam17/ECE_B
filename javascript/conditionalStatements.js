a = 10;
if (a > 5) {
  console.log("a is greater than 5");
}

// odd or even
if (a % 2 == 0) {
  console.log("a is even");
} else {
  console.log("a is odd");
}

// Grade calculator
var mark = 75;
if (mark > 90) {
  console.log("O Grade");
} else if (mark > 70) {
  console.log("A grade");
} else if (mark > 40) {
  console.log("B grade");
} else {
  console.log("Fail");
}